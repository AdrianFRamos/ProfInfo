String getImageForTipo(String tipo) {
  switch (tipo.toLowerCase()) {
    case 'Curso de extensão':
      return 'images/home.jpg';
    case 'Bacharelado':
      return 'images/home.jpg';
    case 'Licenciatura':
      return 'images/home.jpg';
    case 'Tecnólogo':
      return 'images/home.jpg';
    case 'Pós-graduação':
      return 'images/home.jpg';
    case 'Mestrado':
      return 'images/home.jpg';
    case 'Doutorado':
      return 'images/home.jpg';
    default:
      return 'images/home.jpg';
  }
}

    